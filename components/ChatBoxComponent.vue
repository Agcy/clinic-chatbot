      }
    } catch (workletError) {
      console.warn('AudioWorkletÂàùÂßãÂåñÂ§±Ë¥•ÔºåÈôçÁ∫ßÂà∞ScriptProcessor:', workletError.message);
      
      // ÈôçÁ∫ßÂà∞ScriptProcessorÔºàÂ∑≤ÂºÉÁî®‰ΩÜÂÖºÂÆπÊÄßÂ•ΩÔºâ
      processor = audioContext.createScriptProcessor(4096, 1, 1);
      
      processor.onaudioprocess = async (e) => {
        if (!isRecording.value || !sttSessionId) return;
        
        const inputBuffer = e.inputBuffer;
        const inputData = inputBuffer.getChannelData(0);
        
        // ËΩ¨Êç¢‰∏∫16‰ΩçPCMÔºàÂ∞èÁ´ØÂ∫èÔºâ
        const pcmData = new Int16Array(inputData.length);
        for (let i = 0; i < inputData.length; i++) {
          // Á°Æ‰øùÊ†∑Êú¨Âú®[-1, 1]ËåÉÂõ¥ÂÜÖ
          const sample = Math.max(-1, Math.min(1, inputData[i]));
          // ËΩ¨Êç¢‰∏∫16‰ΩçÊï¥Êï∞
          pcmData[i] = sample < 0 ? Math.floor(sample * 0x8000) : Math.floor(sample * 0x7FFF);
        }
        
        // ËΩ¨Êç¢‰∏∫Â≠óËäÇÊï∞ÁªÑÔºàÂ∞èÁ´ØÂ∫èÔºâ
