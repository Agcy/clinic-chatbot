      // console.log('üîá ÂÅúÊ≠¢Èü≥È¢ëËΩ®ÈÅì:', track.kind);
    });
    window.currentAudioStream = null;
  }
  
  // ÂÖ≥Èó≠STT‰ºöËØù
  if (sttSessionId) {
    try {
      await axios.post("/api/speech-to-text-stream", {
